package DefiningClasses.Google;

public class Parens {
    private String name;
    private String bDay;

    public Parens(String name, String bDay) {
        this.name = name;
        this.bDay = bDay;
    }

    public String getName() {
        return name;
    }

    public String getbDay() {
        return bDay;
    }
}
